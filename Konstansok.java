// Konstansok
public class Konstansok {
	public static void main(String[] args) {
		final char UJSOR = '\n';
		final int MODOSITO = 12;
		final float SZORZO = 1.5e-2F;
		final double NORMALO = 150;
		int k;
		double sz;
		k = 32 + MODOSITO;
		sz = 12542.5 * SZORZO/NORMALO;
		System.out.println(" k: " + k + UJSOR + " sz: " + sz + UJSOR);
	}
}
